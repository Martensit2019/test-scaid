/**
 * Интерфейс пользователя
 * Определяет структуру данных пользователя в приложении
 */
export interface User {
  /** Уникальный идентификатор пользователя */
  id: string | number
  /** Имя пользователя */
  firstName: string
  /** Фамилия пользователя */
  lastName: string
  /** Возраст пользователя */
  age: number
  /** Email адрес пользователя */
  email: string
  /** URL фотографии пользователя (опционально) */
  photoUrl?: string
  /** Загруженный файл фотографии (опционально) */
  photoFile?: File
}
